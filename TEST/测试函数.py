def dict_flip(dict_source):
    """
    反转字典
    :param dict_source: 原字典
    :return: 反转后的字典，相同值的添加为列表
    """
    dic_flipped = {}
    for k, v in dict_source.items():
        if v not in dic_flipped:
            dic_flipped[v] = [k]
        else:
            dic_flipped[v].append(k)
    return dic_flipped


